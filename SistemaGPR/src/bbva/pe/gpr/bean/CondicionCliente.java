package bbva.pe.gpr.bean;

public class CondicionCliente {

	private String bureau;
	private String bbvaa;
	private String sistFinan;
	private String relevPubli;
	private String inelegible;
	
	public String getBureau() {
		return bureau;
	}
	public void setBureau(String bureau) {
		this.bureau = bureau;
	}
	public String getBbvaa() {
		return bbvaa;
	}
	public void setBbvaa(String bbvaa) {
		this.bbvaa = bbvaa;
	}
	public String getSistFinan() {
		return sistFinan;
	}
	public void setSistFinan(String sistFinan) {
		this.sistFinan = sistFinan;
	}
	public String getRelevPubli() {
		return relevPubli;
	}
	public void setRelevPubli(String relevPubli) {
		this.relevPubli = relevPubli;
	}
	public String getInelegible() {
		return inelegible;
	}
	public void setInelegible(String inelegible) {
		this.inelegible = inelegible;
	}
	
}
